const os = require('os');
console.log(os.machine()) // return ...
console.log(os.arch()) // return computer architecture
console.log(os.hostname()) // return os host name
console.log(os.platform()) // return os platform
console.log(os.tmpdir()) // return the os default directory for temp files
console.log(os.type()) // return the type of os
console.log(os.userInfo()) // return the current user info as a object
console.log(os.freemem()) // return the free available memory
console.log(os.totalmem()) // return the total memory
console.log(os.homedir()) // return the home directory
